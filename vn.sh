# Function to display colored output with modern styling
print_status() {
    local type=$1
    local message=$2
    
    case $type in
        "INFO") echo -e "${BLUE}üü¶ [INFO]${RESET} ${BOLD}$message${RESET}" ;;
        "WARN") echo -e "${YELLOW}üü® [WARN]${RESET} ${BOLD}$message${RESET}" ;;
        "ERROR") echo -e "${RED}üü• [ERROR]${RESET} ${BOLD}$message${RESET}" ;;
        "SUCCESS") echo -e "${GREEN}üü© [SUCCESS]${RESET} ${BOLD}$message${RESET}" ;;
        "INPUT") echo -e "${CYAN}üü¶ [INPUT]${RESET} ${BOLD}$message${RESET}" ;;
        "DEBUG") echo -e "${PURPLE}üü™ [DEBUG]${RESET} ${BOLD}$message${RESET}" ;;
        *) echo -e "${WHITE}[$type]${RESET} $message" ;;
    esac
}

# Function to display progress bar
show_progress() {
    local current=$1
    local total=$2
    local message=$3
    local width=50
    local percentage=$((current * 100 / total))
    local completed=$((current * width / total))
    local remaining=$((width - completed))
    
    printf "\r${CYAN}${message}:${RESET} [${GREEN}%*s${RESET}${RED}%*s${RESET}] %d%%" \
        $completed "" $remaining "" $percentage
}

